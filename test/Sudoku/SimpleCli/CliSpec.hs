module Sudoku.SimpleCli.CliSpec where

import Test.Hspec
import Sudoku.SimpleCli.Cli (app)

spec :: Spec
spec = it "solves a hard sudoku" $
      app ["......3.."++
           "6...2.1.."++
           ".328.7..."++
           "....4..6."++
           "4.69.85.7"++
           ".9..7...."++
           "...1.582."++
           "..4.8...1"++
           "..5......"] `shouldBe` "\n+-------+-------+-------+"++
                                   "\n| 1 4 8 | 6 5 9 | 3 7 2 |"++
                                   "\n| 6 5 7 | 3 2 4 | 1 9 8 |"++
                                   "\n| 9 3 2 | 8 1 7 | 6 4 5 |"++
                                   "\n+-------+-------+-------+"++
                                   "\n| 7 8 3 | 5 4 1 | 2 6 9 |"++
                                   "\n| 4 2 6 | 9 3 8 | 5 1 7 |"++
                                   "\n| 5 9 1 | 2 7 6 | 4 8 3 |"++
                                   "\n+-------+-------+-------+"++
                                   "\n| 3 7 9 | 1 6 5 | 8 2 4 |"++
                                   "\n| 2 6 4 | 7 8 3 | 9 5 1 |"++
                                   "\n| 8 1 5 | 4 9 2 | 7 3 6 |"++
                                   "\n+-------+-------+-------+\n"
